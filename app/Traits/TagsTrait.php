    {
        $validated = $request->validate([
            'name' => ['required','string','min:3','max:30','unique:tags']
        ]);

        # Update Query Data
        $validated['slug'] = Str::slug($validated['name'],'-');
        # Create Tag
        Tag::create($validated);
        return back()->withSuccess('Tag Created Successfully');
